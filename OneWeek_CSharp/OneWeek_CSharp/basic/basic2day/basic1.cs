
// 変数なしの演算方法.

using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace OneWeek.basic.basic2day
{
    class basic1
    {
        static void Main(string[] args)
        {
            Console.WriteLine("{0} + {1} = {2}", 5, 2, 5 + 2); // 足し算

            /*
             * 今まで%dで入力していたものは{左から何番目なのかの数字}で表記するとのこと.
             *  +	プラス	      足し算を行う演算子	                 5 + 5
             *  -	マイナス	  引き算を行う演算子	                 7 - 3
             *  *	アスタリスク  掛け算を行う演算子	                 7 * 3
             *  /	スラッシュ	  割り算を行う演算子	                 7 / 3
             *  %	パーセント	  剰余（じょうよ）演算子。割り算の余り	 7 % 3
             */
        }
    }
}
