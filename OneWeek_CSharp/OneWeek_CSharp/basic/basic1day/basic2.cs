
// 数字をコンソールに表示する方法.

using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace OneWeek.basic.basic1day
{
    class basic2
    {
        static void Main(string[] args)
        {
            // 数値の表示
            Console.Write(123);
            Console.WriteLine(456);

            // 文字列の表示
            Console.Write("ABC");
            Console.WriteLine("DEF");

            /*
             * ---WriteとWriteLineの違い
             *  Writeは文字の後改行しない
             *  WriteLineは文字の後改行する
             */
        }
    }
}
